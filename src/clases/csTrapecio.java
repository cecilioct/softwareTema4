public class csTrapecio  {
    int j;
    double ajh,fx ;
    double fa,fb;
    double a,b,n;
    csTrapecio(){
    }
    csTrapecio(double a, double b,double n){
        this.a=a;
        this.b=b;
        this.n=n;
    }

    public int getj() {
        return j;
    }

    public void setJ(int j) {
        this.j = j;
    }

    public double getAjh() {
        return ajh;
    }

    public void setAjh(double ajh) {
        this.ajh = ajh;
    }

    public double getFx() {
        return fx;
    }

    public void setFx(double fx) {
        this.fx = fx;
    }

    public double getFa() {
        return fa;
    }

    public void setFa(double fa) {
        this.fa = fa;
    }

    public double getFb() {
        return fb;
    }

    public void setFb(double fb) {
        this.fb = fb;
    }

    public double getA() {
        return a;
    }

    public void setA(double a) {
        this.a = a;
    }

    public double getB() {
        return b;
    }

    public void setB(double b) {
        this.b = b;
    }

    public double getN() {
        return n;
    }

    public void setN(double n) {
        this.n = n;
    }
    
    
    
    
    
}
