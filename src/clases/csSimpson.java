public class csSimpson {
    double Xi,Xf,h;
    double x,y;
    int i,n;
    public csSimpson(double Xi, double Xf, int n) {
        this.Xi = Xi;
        this.Xf = Xf;
        this.n = n;
    }
    public csSimpson(){
    }

    public double getXi() {
        return Xi;
    }

    public void setXi(double Xi) {
        this.Xi = Xi;
    }

    public double getXf() {
        return Xf;
    }

    public void setXf(double Xf) {
        this.Xf = Xf;
    }

    public double getH() {
        return h;
    }

    public void setH(double h) {
        this.h = h;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }

    



    
    
}
